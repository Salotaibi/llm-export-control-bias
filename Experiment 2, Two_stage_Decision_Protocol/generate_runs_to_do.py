def split_fixed_partners(fp_value):
    if isinstance(fp_value, float) and math.isnan(fp_value):
        return []
    if fp_value is None:
        return []
    txt = str(fp_value).strip()
    if not txt:
        return []
    return [p.strip() for p in txt.split(";") if p.strip()]

def est_token_len(s):
    return int(len(s) / 4)  # rough heuristic

def make_run_id(project_id, iso3, condition, template_variant, stage, prompt_text):
    h = hashlib.sha1()
    key = f"{project_id}|{iso3}|{condition}|{template_variant}|{stage}".encode("utf-8")
    h.update(key)
    h.update(prompt_text.encode("utf-8"))
    return h.hexdigest()

