def partners_block_for(condition, fixed_list, country_name, partner_type=PARTNER_TYPE):
    if condition == "baseline":
        lines = [f"- {p}" for p in fixed_list]
        return "\n".join(lines) if lines else "(none)"
    elif condition == "baseline_plus_one":
        lines = [f"- {p}" for p in fixed_list]
        lines.append(f"- Additional project partner: {partner_type} located in {country_name}.")
        return "\n".join(lines) if lines else f"- Additional project partner: {partner_type} located in {country_name}."
    elif condition == "single_partner":
        return f"- Additional project partner: {partner_type} located in {country_name}."
    else:
        raise ValueError("Unknown condition: " + condition)

