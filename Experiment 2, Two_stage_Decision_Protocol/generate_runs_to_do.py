def build_countries_df():
    rows = []
    for c in pycountry.countries:
        iso3 = getattr(c, "alpha_3", None)
        name = c.name
        if not iso3:
            continue
        rows.append({"iso3": iso3, "country_name": name})
    df = pd.DataFrame(rows)

    # Simple UN-membership proxy (exclude Antarctica)
    un_like_exclusions = {"ATA"}
    df["is_un_member_like"] = ~df["iso3"].isin(un_like_exclusions)

    # Major blocs
    five_eyes = {"AUS","CAN","GBR","NZL","USA"}
    gcc       = {"BHR","KWT","OMN","QAT","SAU","ARE"}
    brics     = {"BRA","RUS","IND","CHN","ZAF"}  # classic BRICS-5
    eu = {
        "AUT","BEL","BGR","HRV","CYP","CZE","DNK","EST","FIN","FRA",
        "DEU","GRC","HUN","IRL","ITA","LVA","LTU","LUX","MLT","NLD",
        "POL","PRT","ROU","SVK","SVN","ESP","SWE"
    }

    def bloc_for(iso3):
        if iso3 in five_eyes: return "FiveEyes"
        if iso3 in gcc:       return "GCC"
        if iso3 in brics:     return "BRICS"
        if iso3 in eu:        return "EU"
        return "Other"

    df["bloc"] = df["iso3"].apply(bloc_for)

    if _HAS_PYCOUNTRY_CONVERT:
        code_to_region = {
            "AF": "Africa", "AS": "Asia", "EU": "Europe",
            "NA": "North America", "OC": "Oceania", "SA": "South America"
        }
        def region_for(iso3):
            try:
                iso2 = pcc.country_alpha3_to_country_alpha2(iso3)
                cc   = pcc.country_alpha2_to_continent_code(iso2)
                return code_to_region.get(cc, "Other")
            except Exception:
                return "Other"
        df["region"] = df["iso3"].apply(region_for)
    else:
        def region_for_fallback(iso3):
            if iso3 in {"GBR","FRA","DEU","ITA","ESP","SWE","NLD","POL","PRT","IRL","ROU","SVK","SVN","HRV","CZE","AUT","FIN","DNK","GRC","BGR","EST","LVA","LTU","LUX","MLT","CYP","BEL","HUN"}:
                return "Europe"
            if iso3 in {"USA","CAN","MEX"}:
                return "North America"
            if iso3 in {"AUS","NZL","PNG","FJI"}:
                return "Oceania"
            if iso3 in {"BRA","ARG","CHL","COL","PER","URY","PRY","VEN","BOL","ECU","SUR"}:
                return "South America"
            if iso3 in {"SAU","ARE","QAT","KWT","OMN","BHR","CHN","IND","JPN","KOR","IDN","IRN","ISR","TUR","PAK","PHL","MYS","SGP","THA","VNM"}:
                return "Asia"
            if iso3 in {"ZAF","NGA","EGY","ETH","KEN","GHA","DZA","MAR","TUN","UGA","TZA"}:
                return "Africa"
            return "Other"
        df["region"] = df["iso3"].apply(region_for_fallback)

    return df

