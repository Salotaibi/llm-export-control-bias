# -------- Helpers: Composite builder (2×3 grid) --------
def make_composite(image_paths, title_text, out_path, grid=(2,3), tile_size=(1400,900), pad=16, title_tile=True):
    """
    Combine up to 5 images into a 2x3 grid. The sixth tile is a title panel.
    image_paths: list of file paths (<=5)
    """
    cols = grid[1]; rows = grid[0]
    W, H = tile_size
    canvas = Image.new("RGB", (cols*W + (cols+1)*pad, rows*H + (rows+1)*pad), (255,255,255))

    # Paste up to 5 images
    for idx, pth in enumerate(image_paths[:5]):
        r = idx // cols
        c = idx %  cols
        x = pad + c*(W+pad)
        y = pad + r*(H+pad)
        try:
            im = Image.open(pth).convert("RGB")
            im = im.resize((W, H), Image.LANCZOS)
            canvas.paste(im, (x, y))
        except Exception as e:
            # draw placeholder
            draw = ImageDraw.Draw(canvas)
            draw.rectangle([x, y, x+W, y+H], outline=(0,0,0), width=2)
            draw.text((x+20, y+20), f"Missing:\n{os.path.basename(pth)}", fill=(0,0,0))
    # Sixth tile as title panel
    if title_tile:
        x = pad + (cols-1)*(W+pad)
        y = pad + (rows-1)*(H+pad)
        draw = ImageDraw.Draw(canvas)
        draw.rectangle([x, y, x+W, y+H], fill=(248,248,248), outline=(0,0,0), width=2)
        # try to load a font; fallback to default
        try:
            font = ImageFont.truetype("DejaVuSans-Bold.ttf", 48)
            font2= ImageFont.truetype("DejaVuSans.ttf", 32)
        except:
            font = None
            font2= None
        draw.text((x+40, y+40), title_text, fill=(0,0,0), font=font if font else None)
        draw.text((x+40, y+120), f"{TEMPERATURE_TAG.upper()}  •  {len(image_paths)} models", fill=(40,40,40), font=font2 if font2 else None)

    canvas.save(out_path, quality=95)
    print("Composite saved:", out_path)

