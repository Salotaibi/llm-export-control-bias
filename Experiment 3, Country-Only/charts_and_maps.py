# -------- Robust Natural Earth downloader (for maps) --------
def ensure_natural_earth():
    NE_MIRRORS = [
        "https://naturalearth.s3.amazonaws.com/110m_cultural/ne_110m_admin_0_countries.zip",
        "https://github.com/nvkelso/natural-earth-vector/releases/download/v5.0.1/ne_110m_admin_0_countries.zip",
        "https://cdn.jsdelivr.net/gh/nvkelso/natural-earth-vector@master/geojson/ne_110m_admin_0_countries.zip",
    ]
    NE_ZIP = os.path.join(OUT_DIR, "ne_110m_admin_0_countries.zip")
    NE_DIR = os.path.join(OUT_DIR, "ne_110m_admin_0_countries")

    if os.path.isdir(NE_DIR):
        for fn in os.listdir(NE_DIR):
            if fn.endswith(".shp"):
                return os.path.join(NE_DIR, fn)

    os.makedirs(NE_DIR, exist_ok=True)
    last_err = None
    headers = {"User-Agent": "Mozilla/5.0 (X11; Linux x86_64) ColabPlotter/1.0"}
    for url in NE_MIRRORS:
        try:
            print("Downloading:", url)
            r = requests.get(url, timeout=60, headers=headers)
            r.raise_for_status()
            with zipfile.ZipFile(io.BytesIO(r.content)) as z:
                z.testzip()
                z.extractall(NE_DIR)
            for fn in os.listdir(NE_DIR):
                if fn.endswith(".shp"):
                    return os.path.join(NE_DIR, fn)
        except Exception as e:
            last_err = e
            print("  -> failed, trying next…", e)
    raise SystemExit(f"All Natural Earth mirrors failed. Last error: {last_err}")

