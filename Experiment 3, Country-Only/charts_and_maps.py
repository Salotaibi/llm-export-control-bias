        # robust CIs
        ci_low_s  = pd.to_numeric(sub["ci_low"],  errors="coerce")
        ci_high_s = pd.to_numeric(sub["ci_high"], errors="coerce")
        y_series  = pd.Series(y, index=sub.index)
        ci_low  = ci_low_s.combine_first(y_series).to_numpy(float)
        ci_high = ci_high_s.combine_first(y_series).to_numpy(float)
        lower_err = np.maximum(0.0, y - ci_low)
        upper_err = np.maximum(0.0, ci_high - y)
        yerr = np.vstack([lower_err, upper_err])

        plt.figure(figsize=(7.0, 4.6), dpi=220)
        plt.bar(x, y)
        plt.errorbar(x, y, yerr=yerr, fmt="none", capsize=3, linewidth=1)
