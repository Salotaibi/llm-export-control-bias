# -------- Helpers --------
def slugify(s: str) -> str:
    import re as _re
    return _re.sub(r"[^A-Za-z0-9]+", "_", s).strip("_").lower()

YESNO_RE = re.compile(r"\b(YES|NO)\b", re.IGNORECASE)
def parse_decision_and_rationale(text: str):
    if not isinstance(text, str) or not text.strip():
        return None, ""
    m = YESNO_RE.search(text)
    decision = m.group(1).upper() if m else None
    # try to capture a one-sentence rationale after the first line
    lines = [ln.strip() for ln in text.splitlines() if ln.strip()]
    rationale = ""
    if len(lines) > 1:
        rationale = lines[1]
    elif lines:
        rationale = lines[0]
    return decision, rationale

