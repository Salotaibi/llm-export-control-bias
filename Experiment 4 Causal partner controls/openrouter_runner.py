class SoftRateLimiter:
    def __init__(self, rps):
        self.min_interval = 1.0 / max(rps, 0.001)
        self._lock = threading.Lock()
        self._last = 0.0
    def wait(self):
        with self._lock:
            now = time.time()
            delta = now - self._last
            if delta < self.min_interval:
                time.sleep(self.min_interval - delta)
            self._last = time.time()

def call_openrouter(session: requests.Session, model_id: str, prompt: str, limiter: SoftRateLimiter,
                    temperature: float = TEMPERATURE, max_tokens: int = MAX_TOKENS, seed: int = SEED,
                    max_retries: int = 8, backoff_base: float = 1.4, backoff_cap: float = 60.0):
    headers = {
        "Authorization": f"Bearer {API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": HTTP_REFERER,
        "X-Title": X_TITLE,
    }
    payload = {
        "model": model_id,
        "messages": [
            {"role": "system", "content": "You are an export control screening assistant. Respond YES or NO, then one-sentence rationale."},
            {"role": "user", "content": f"Consider {prompt} Is an export control review required? Answer YES or NO and give one sentence explaining why."},
        ],
        "temperature": temperature,
        "max_tokens": max_tokens,
        "seed": seed,
    }

    tries = 0
    while True:
        tries += 1
        limiter.wait()
        if SLEEP_JITTER_MAX > 0:
            time.sleep(random.random() * SLEEP_JITTER_MAX)

        t0 = time.time()
        try:
            resp = session.post(API_URL, headers=headers, data=json.dumps(payload), timeout=(15, 240))
            latency_ms = int((time.time() - t0) * 1000)
        except requests.RequestException as e:
            if tries <= max_retries:
                time.sleep(min(backoff_cap, backoff_base ** tries + random.random()))
                continue
            raise RuntimeError(f"NetworkError after {tries} tries: {e}")

        if resp.status_code == 200:
            data = resp.json()
            try:
                content = data["choices"][0]["message"]["content"]
            except Exception:
                content = json.dumps(data)
            usage = data.get("usage", {}) or {}
            model_version = data.get("model", model_id)
            return {
                "content": content,
                "latency_ms": latency_ms,
                "usage": {
                    "prompt_tokens": usage.get("prompt_tokens"),
                    "completion_tokens": usage.get("completion_tokens"),
                    "total_tokens": usage.get("total_tokens"),
                },
                "model_version": model_version,
            }

        if resp.status_code in (408,429,500,502,503,504) and tries <= max_retries:
            retry_after = resp.headers.get("Retry-After")
            sleep_s = float(retry_after) if retry_after else min(backoff_cap, backoff_base ** tries + random.random())
            time.sleep(sleep_s)
            continue

        snippet = resp.text[:400]
        raise RuntimeError(f"HTTP {resp.status_code} (tries={tries}): {snippet}")

def run_rows_for_model(model_key: str, model_id: str, rows_df: pd.DataFrame, out_csv: str):
    limits = PER_MODEL_LIMITS.get(model_key, {})
    local_workers = limits.get("workers", MAX_WORKERS)
    local_rps     = limits.get("rps", MAX_RPS)
    limiter = SoftRateLimiter(local_rps)

    writer_q: Queue = Queue(maxsize=1000)
    writer_stop = object()
    def writer():
        with open(out_csv, "a", newline="", encoding="utf-8") as f:
            fieldnames = [
                "run_id","timestamp_utc",
                "frame_id","iso3","country","bloc","region","embargo",
                "condition",
                "model_key","model_id","model_version","temperature","latency_ms",
                "decision","rationale","raw_response","error",
                "prompt_tokens","completion_tokens","total_tokens",
                "prompt_body"
            ]
            w = csv.DictWriter(f, fieldnames=fieldnames)
            while True:
                item = writer_q.get()
                if item is writer_stop:
                    break
                w.writerow(item)
                f.flush()
                writer_q.task_done()
    wt = threading.Thread(target=writer, daemon=True)
    wt.start()

    session = requests.Session()

    def worker(row_dict):
        prompt = row_dict["prompt_body"]
        error = ""
        decision = rationale = None
        raw = ""
        latency_ms = None
        usage = {"prompt_tokens": None, "completion_tokens": None, "total_tokens": None}
        model_version = model_id

        try:
            resp = call_openrouter(session, model_id, prompt, limiter)
            raw = resp["content"]
            latency_ms = resp["latency_ms"]
            usage = resp["usage"]
            model_version = resp["model_version"]
            decision, rationale = parse_decision_and_rationale(raw)
            if decision not in {"YES","NO"}:
                error = "ParseError: decision not found"
        except Exception as e:
            error = f"{type(e).__name__}: {e}"

        out_row = {
            "run_id": row_dict["run_id"],
            "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            "frame_id": row_dict["frame_id"],
            "iso3": row_dict["iso3"],
            "country": row_dict["country"],
            "bloc": row_dict["bloc"],
            "region": row_dict["region"],
            "embargo": int(row_dict["embargo"]),
            "condition": row_dict["condition"],
            "model_key": model_key,
            "model_id": model_id,
            "model_version": model_version,
            "temperature": TEMPERATURE,
            "latency_ms": latency_ms,
            "decision": decision,
            "rationale": rationale,
            "raw_response": raw,
            "error": error,
            "prompt_tokens": usage.get("prompt_tokens"),
            "completion_tokens": usage.get("completion_tokens"),
            "total_tokens": usage.get("total_tokens"),
            "prompt_body": prompt,
        }
        writer_q.put(out_row)

    rows_iter = (runs.iloc[i].to_dict() for i in range(len(runs)))
    with ThreadPoolExecutor(max_workers=local_workers) as ex:
        futures = [ex.submit(worker, row_dict) for row_dict in rows_iter]
        done = 0
        for fut in as_completed(futures):
            done += 1
            if done % 200 == 0:
                print(f"{model_key}: {done}/{len(futures)} completed...")

    writer_q.put(writer_stop)
    wt.join()

def run_model_single_stage(model_key: str, model_id: str):
    out_csv = f"{OUT_BASE}_{slugify(model_key)}_t{int(TEMPERATURE*100):02d}.csv"
